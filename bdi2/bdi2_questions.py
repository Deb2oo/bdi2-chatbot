BDI2_INTERPRETATION = {
    (0, 10): "These ups and downs are considered normal",
    (11, 16): "Mild mood disturbance", 
    (17, 20): "Borderline clinical depression",
    (21, 30): "Moderate depression",
    (31, 40): "Severe depression",
    (41, 63): "Extreme depression"
}

def get_depression_level(score):
    """Get depression level interpretation based on BDI-II score"""
    for (min_score, max_score), level in BDI2_INTERPRETATION.items():
        if min_score <= score <= max_score:
            return level
    return "Score out of range"